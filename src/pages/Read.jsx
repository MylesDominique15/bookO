// ─── Nav hint bar ─────────────────────────────────────────────────────────────
function NavHint({ onNext, onPrev, activeIndex, total }) {
  return (
    <div className="flex items-center justify-between mt-6 px-4 py-3 rounded-2xl bg-orange-50 border border-orange-100">
      <button
        onClick={onPrev}
        disabled={activeIndex === 0}
        className="flex items-center gap-1.5 px-3 py-1.5 rounded-xl text-xs font-semibold text-slate-500 hover:text-orange-500 disabled:opacity-30 disabled:cursor-not-allowed transition-colors duration-150"
      >
        <ChevronUp size={13} /> Prev
      </button>
      <span className="text-[11px] text-slate-400 font-medium">
        Line {activeIndex + 1} of {total} · <kbd className="font-semibold text-orange-400">Space</kbd> or <kbd className="font-semibold text-orange-400">↓</kbd> to advance
      </span>
      <button
        onClick={onNext}
        disabled={activeIndex >= total - 1}
        className="flex items-center gap-1.5 px-3 py-1.5 rounded-xl text-xs font-semibold text-slate-500 hover:text-orange-500 disabled:opacity-30 disabled:cursor-not-allowed transition-colors duration-150"
      >
        Next <ChevronDown size={13} />
      </button>
    </div>
  );
}

// ─── Main Read Page ───────────────────────────────────────────────────────────
export default function Read() {
  const navigate = useNavigate();
  const { font, toggleFont, FONTS: FONT_OPTIONS } = useTheme();
  const { extractedText, documentTitle } = useContext(ReaderContext);

  // ── STATE & LOGIC: UNCHANGED ───────────────────────────────────────────────
  const [audioEnabled, setAudioEnabled] = useState(false);
  const [modesEnabled, setModesEnabled] = useState(false);

  const textToRender = extractedText || SAMPLE_TEXT;
  const { containerRef, lines, isReady, reparse } = useLineParser(textToRender);

  const readingAreaRef = useRef(null);
  const { activeLineIndex, goToNext, goToPrev, reset, isComplete } =
    useReaderNavigation(lines.length, isReady, readingAreaRef);

  const lineRefs = useRef([]);
  useEffect(() => {
    const el = lineRefs.current[activeLineIndex];
    if (el) el.scrollIntoView({ block: "center", behavior: "smooth" });
  }, [activeLineIndex]);

  useEffect(() => {
    let timeout;
    const onResize = () => { clearTimeout(timeout); timeout = setTimeout(reparse, 200); };
    window.addEventListener("resize", onResize);
    return () => { window.removeEventListener("resize", onResize); clearTimeout(timeout); };
  }, [reparse]);
  // ── END LOGIC ──────────────────────────────────────────────────────────────

  const progressPct = lines.length > 1
    ? Math.round((activeLineIndex / (lines.length - 1)) * 100)
    : 0;

  const isOD = font === FONT_OPTIONS.OPEN_DYSLEXIC;

  return (
    <>
      <style>{`
        @keyframes fadeSlideUp {
          from { opacity: 0; transform: translateY(20px); }
          to   { opacity: 1; transform: translateY(0); }
        }
        .read-line {
          transition: background 0.3s ease, color 0.3s ease,
                      opacity 0.3s ease, transform 0.25s ease,
                      box-shadow 0.3s ease;
        }
      `}</style>

      {/* ── Full page shell ─────────────────────────────────────────────── */}
      <div className="flex h-screen overflow-hidden bg-orange-50/60">

        {/* ── Green progress bar — fixed across very top ─────────────────── */}
        <div className="fixed top-0 left-0 right-0 h-1 bg-green-100 z-50">
          <div
            className="h-full bg-green-500 rounded-full transition-all duration-500 ease-out"
            style={{ width: `${progressPct}%` }}
            role="progressbar"
            aria-valuenow={progressPct}
            aria-valuemin={0}
            aria-valuemax={100}
          />
        </div>

        {/* ── LEFT SIDEBAR ────────────────────────────────────────────────── */}
        <aside
          className="w-56 flex-shrink-0 flex flex-col bg-amber-100/70 border-r border-orange-200/60 px-4 py-6 gap-5 overflow-y-auto pt-5"
          aria-label="Reading controls"
        >
          {/* Back arrow */}
          <button
            onClick={() => navigate("/")}
            aria-label="Back to home"
            className="w-9 h-9 flex items-center justify-center rounded-full bg-white border border-orange-200 text-orange-400 hover:bg-orange-400 hover:text-white hover:border-orange-400 shadow-sm transition-all duration-200 self-start"
          >
            <ArrowLeft size={16} />
          </button>

          {/* Sprite */}
          <Sprite />

          {/* Divider */}
          <div className="h-px bg-orange-200/60 mx-1" />

          {/* Toggle buttons */}
          <div className="flex flex-col gap-2">
            <SidebarToggle
              icon={<Type size={14} />}
              label="OpenDyslexic"
              active={isOD}
              onClick={toggleFont}
            />
            <SidebarToggle
              icon={<Volume2 size={14} />}
              label="Audio Option"
              active={audioEnabled}
              onClick={() => setAudioEnabled((v) => !v)}
            />
            <SidebarToggle
              icon={<Sun size={14} />}
              label="Modes"
              active={modesEnabled}
              onClick={() => setModesEnabled((v) => !v)}
            />
          </div>

          {/* Pomodoro — pinned to bottom */}
          <div className="mt-auto">
            <PomodoroTimer />
          </div>
        </aside>

        {/* ── MAIN CONTENT AREA ───────────────────────────────────────────── */}
        <main
          ref={readingAreaRef}
          className="flex-1 overflow-y-auto pt-7 pb-16 px-6 flex flex-col items-center"
          tabIndex={-1}
          aria-label="Reading content"
        >
          {/* Hidden measuring container — needed by useLineParser */}
          <div
            ref={containerRef}
            aria-hidden="true"
            className="absolute invisible pointer-events-none text-lg leading-loose"
            style={{
              width: "min(680px, calc(100vw - 224px - 48px))",
              fontFamily: isOD ? "'OpenDyslexic', sans-serif" : "inherit",
              whiteSpace: "normal",
              wordBreak: "break-word",
            }}
          />

          {/* ── Document "paper" card ───────────────────────────────────── */}
          <div
            className="w-full max-w-2xl bg-white rounded-3xl shadow-2xl shadow-orange-100/80 px-10 pt-9 pb-10"
            style={{
              fontFamily: isOD ? "'OpenDyslexic', sans-serif" : "'Georgia', serif",
            }}
          >
            {/* Document header */}
            <header className="mb-6 border-b border-orange-100 pb-5">
              <p className="text-[10px] font-extrabold tracking-[0.16em] uppercase text-orange-300 mb-1.5">
                Academic Reading
              </p>
              <h1 className="text-2xl font-bold text-slate-800 leading-tight tracking-tight">
                {documentTitle || "bookO"}
              </h1>

              {/* Progress bar inside card */}
              {isReady && lines.length > 0 && (
                <div className="flex items-center gap-3 mt-4">
                  <div className="flex-1 h-1.5 bg-orange-100 rounded-full overflow-hidden">
                    <div
                      className="h-full bg-orange-400 rounded-full transition-all duration-500 ease-out"
                      style={{ width: `${progressPct}%` }}
                    />
                  </div>
                  <span className="text-[11px] text-orange-400 font-bold tabular-nums min-w-[32px] text-right">
                    {progressPct}%
                  </span>
                </div>
              )}
            </header>

            {/* Skeleton while parsing */}
            {!isReady && (
              <div className="flex flex-col gap-3 animate-pulse">
                {[...Array(7)].map((_, i) => (
                  <div
                    key={i}
                    className="h-5 bg-orange-50 rounded-lg"
                    style={{ width: i % 3 === 2 ? "55%" : "100%" }}
                  />
                ))}
              </div>
            )}

            {/* Rendered lines */}
            {isReady && (
              <div role="article" className="flex flex-col gap-0.5">
                {lines.map((line, i) => (
                  <div
                    key={i}
                    ref={(el) => (lineRefs.current[i] = el)}
                    className="read-line text-[17px] leading-loose px-3 py-1 cursor-default"
                    style={getLineStyle(i, activeLineIndex)}
                    role="paragraph"
                    aria-current={i === activeLineIndex ? "true" : undefined}
                    onClick={() => {
                      if (i === activeLineIndex + 1) goToNext();
                      if (i === activeLineIndex - 1) goToPrev();
                    }}
                  >
                    {line.text}
                  </div>
                ))}
              </div>
            )}

            {/* Completion banner */}
            {isReady && isComplete && lines.length > 0 && (
              <CompletionBanner
                onAssessment={() => navigate("/assessment")}
                onRestart={reset}
              />
            )}

            {/* Nav hint */}
            {isReady && lines.length > 0 && !isComplete && (
              <NavHint
                onNext={goToNext}
                onPrev={goToPrev}
                activeIndex={activeLineIndex}
                total={lines.length}
              />
            )}
          </div>
        </main>
      </div>
    </>
  );
}