// ─── Sample text ──────────────────────────────────────────────────────────────
const SAMPLE_TEXT = `Cognitive load theory, developed by John Sweller in the late 1980s,
is a framework that describes how the human brain processes and stores information during
learning. The theory posits that working memory, the cognitive system responsible for
temporarily holding and manipulating information, has a strictly limited capacity. When
instructional material overwhelms this capacity, learning becomes inefficient or fails
entirely. Sweller identified three distinct types of cognitive load that operate
simultaneously during any learning episode. Intrinsic load refers to the inherent
complexity of the subject matter itself and the degree to which its elements interact
with one another. Extraneous load is generated by the design of instructional materials
and does not contribute to learning; it is imposed by poor presentation, unnecessary
decorative elements, or redundant information. Germane load, the most valuable of the
three, is the mental effort dedicated to constructing and automating schemas, which are
organized knowledge structures stored in long-term memory. Effective instructional design
seeks to reduce extraneous load as much as possible, manage intrinsic load through
sequencing and scaffolding, and redirect freed cognitive resources toward germane
processing. Research in this domain has produced a number of empirically validated
principles, including the worked example effect, the split-attention effect, the
redundancy effect, and the expertise reversal effect. These findings have profound
implications for educators, interface designers, and anyone tasked with communicating
complex information to a human audience.`;

// ─── Line highlight helper (uses inline styles for dynamic colors) ─────────────
function getLineStyle(lineIndex, activeIndex) {
  if (lineIndex === activeIndex) {
    return {
      background: "#f97316",       // orange-500
      color: "#ffffff",
      borderRadius: "10px",
      transform: "scale(1.01)",
      boxShadow: "0 4px 18px rgba(249,115,22,0.28)",
      opacity: 1,
    };
  }
  if (lineIndex === activeIndex - 1 || lineIndex === activeIndex + 1) {
    return {
      background: "#fed7aa",       // orange-200
      color: "#7c2d12",            // orange-950
      borderRadius: "10px",
      transform: "scale(1)",
      opacity: 0.75,
    };
  }
  return {
    background: "transparent",
    color: "#374151",              // gray-700
    opacity: 0.25,
    borderRadius: "10px",
    transform: "scale(1)",
  };
}

// ─── Sidebar toggle button ────────────────────────────────────────────────────
function SidebarToggle({ icon, label, active, onClick }) {
  return (
    <button
      onClick={onClick}
      className={[
        "flex items-center gap-3 w-full px-4 py-3 rounded-2xl text-sm font-semibold",
        "border transition-all duration-200 text-left",
        active
          ? "bg-orange-400 text-white border-orange-400 shadow-md shadow-orange-200"
          : "bg-white text-slate-600 border-orange-100 hover:border-orange-300 hover:text-orange-500",
      ].join(" ")}
    >
      <span className={active ? "text-white" : "text-orange-400"}>{icon}</span>
      {label}
      {/* Pill indicator */}
      <span
        className={[
          "ml-auto w-8 h-4 rounded-full flex items-center px-0.5 transition-all duration-300",
          active ? "bg-white/30 justify-end" : "bg-orange-100 justify-start",
        ].join(" ")}
      >
        <span
          className={[
            "w-3 h-3 rounded-full transition-all duration-300",
            active ? "bg-white" : "bg-orange-300",
          ].join(" ")}
        />
      </span>
    </button>
  );
}

